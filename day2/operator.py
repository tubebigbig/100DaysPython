import math

print(f"2 + 3 = {2 + 3}")
print(f"2 - 3 = {2 - 3}")
print(f"2 * 3 = {2 * 3}")
print(f"2 ** 3 = {2 ** 3}")
print(f"6 / 2 = {6 / 2}")
print(f"6 // 2 = {6 // 2}")
print(f"6 % 2 = {6 % 2}")
print(f"ceil (8/3) = {math.ceil(8 / 3)}")
print(f"floor (8/3) = {math.floor(8 / 3)}")
print(f"round (8/3) = {round(8 / 3)}")
print(f"round (8/3, 2) = {round(8 / 3, 2)}")
number = 100
print(f"number - 2 = {number - 2}")
number -= 2
print(f"number -= 2 = {number}")
